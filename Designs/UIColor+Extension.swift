import UIKit

extension UIColor {
    static var ppkeBlue: UIColor {
        return UIColor(red: 0.0/255.0, green: 206.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
    
    static var ppkeGrey: UIColor {
        return UIColor(red: 239.0/255.0, green: 238.0/255.0, blue: 238.0/255.0, alpha: 1.0)
    }
}
